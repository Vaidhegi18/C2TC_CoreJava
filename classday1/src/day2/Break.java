package day2;
//break and continue

public class Break {
	public static void main(String args[]) {
		for(int i=1;i<=5;i++) {
		System.out.println("break method");
		break; //if you have to stop printing you have to use break method
		 	 
	}
		for(int j=1;j<=5;j++) {
			System.out.println("continue method");
			continue;	
	}
}
}