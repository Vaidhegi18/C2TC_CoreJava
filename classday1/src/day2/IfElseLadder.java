//if else ladder 

//if-else-if ladder is used to decide among multiple options

public class IfElseLadder {

	public static void main(String[] args) {
		int p = 50;       
		if(p==20) 
			System.out.println("check p==20");
		
		else if(p==30) 
			System.out.println("check p==30");
		
		else if(p==50) 
			System.out.println("check p==50");
		
		else 
			System.out.println("otherwise this will be executed");
		
			System.out.println("this is ladder method");
		}	
	}


